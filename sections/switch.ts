export let weekDay : number = -5;

switch (weekDay) {
    // weekDay ===1
    case 1: 
    console.log("Es Lunes");
    break; // Se agrega break para evitar que se ejecuten los siguientes casos

    case 2:
        // weekDay ===2
    console.log("Es Martes");
    break;

    // weekDay ===3
    case 3:
    console.log("Es Miercoles");
    break;

    default:
    console.log("No es Lunes, Martes o Miercoles");
        
}





